import { formatTime } from '../formatTime';

describe('formatTime', () => {

  // 正常な時間文字列のテスト（時間のみ）
  it('時間文字列 "2024-08-15T08:30:00" を "08:30" にフォーマットする', () => {
    expect(formatTime('2024-08-15T08:30:00')).toBe('08:30'); // JST 08:30 のテスト
  });

  it('時間文字列 "2024-08-15T00:05:00" を "00:05" にフォーマットする', () => {
    expect(formatTime('2024-08-15T00:05:00')).toBe('00:05'); // JST 00:05 のテスト
  });

  it('時間文字列 "2024-08-15T23:59:00" を "23:59" にフォーマットする', () => {
    expect(formatTime('2024-08-15T23:59:00')).toBe('23:59'); // JST 23:59 のテスト
  });

  // ツールチップ用のフォーマット（日本標準時基準の日付と時間を含む）
  it('時間文字列 "2024-08-15T08:30:00" を "08/15 08:30" にフォーマットする', () => {
    expect(formatTime('2024-08-15T08:30:00', true)).toBe('08/15 08:30'); // 日付と時間をJSTでフォーマット
  });

  it('時間文字列 "2024-12-31T23:59:00" を "12/31 23:59" にフォーマットする', () => {
    expect(formatTime('2024-12-31T23:59:00', true)).toBe('12/31 23:59'); // 年末年始の日付と時間をJSTでフォーマット
  });

  // 無効な時間文字列のテスト
  it('無効な時間文字列をフォーマットした場合に "NaN:NaN" を返す', () => {
    expect(formatTime('invalid-time-string')).toBe('NaN:NaN'); // 無効な時間文字列のテスト
  });

  it('空の時間文字列をフォーマットした場合に "NaN:NaN" を返す', () => {
    expect(formatTime('')).toBe('NaN:NaN'); // 空の文字列のテスト
  });

});
